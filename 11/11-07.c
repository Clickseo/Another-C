#include <stdio.h>
#define MAX_SIZE 3
int main(void)
{
	int	i, a, b, c;
	int	*arr[MAX_SIZE] = {&a, &b, &c};
	int	**pArr;

	pArr = arr;		// pArr = &arr[0];

	printf("%3d개의 정수 입력 : ", MAX_SIZE);
	for(i=0; i<MAX_SIZE; i++)
		scanf("%d", *(pArr+i));

	printf("\n### 결과 출력 ### \n");
	for(i=0; i<MAX_SIZE; i++)
		printf("%d \n", **(pArr+i));

	return 0;
}
