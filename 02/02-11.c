#include <stdio.h>
int main(void)
{
	int	a = 10;		// a --> |0000 0000|0000 0000|0000 0000|0000 1010|
	int	b = -128;	// b --> |1111 1111|1111 1111|1111 1111|1000 0000|

	// 2의 보수 연산
	a = ~a + 1;		// ~a --> |1111 1111|1111 1111|1111 1111|1111 0101|
	b = ~b + 1;		//  b --> |0000 0000|0000 0000|0000 0000|0111 1111|

	// ~a + 1 --> |1111 1111|1111 1111|1111 1111|1111 0110|
	// ~b + 1 --> |0000 0000|0000 0000|0000 0000|1000 0000|
	printf("a : %d \n", a);		// -10
	printf("b : %d \n", b);		// 128

	return 0;
}
